    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        String rollNo =rollno_txt.getText();
        try{
            Connection con = Connection_code.get_con();
            Statement st = con.prepareStatement("create", ResultSet.TYPE_SCROLL_SENSITIVE,
                ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("select * from student natural join result where rollno =" + rollNo + ";");
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Entered rollNo doesn't exist");
            }
            else{
                //rollno | name   | gender | father_name | course | branch_name | physics | maths | chem | aptitude | english | softskill | total
                name_txt.setText(rs.getString(2));
                gender_txt.setText(rs.getString(3));
                father_name_txt.setText(rs.getString(4));
                course_txt.setText(rs.getString(5));
                branch_txt.setText(rs.getString(6));
                
                physics_txt.setText(rs.getString(7));
                maths_txt.setText(rs.getString(8));
                chemistry_txt.setText(rs.getString(9));
                aptitude_txt.setText(rs.getString(10));
                english_txt.setText(rs.getString(11));
                softSkills_txt.setText(rs.getString(12));
                total_marks_txt.setText(rs.getString(13));
                
                name_txt.setEditable(false);
                gender_txt.setEditable(false);
                father_name_txt.setEditable(false);
                course_txt.setEditable(false);
                branch_txt.setEditable(false);
                
                physics_txt.setEditable(false);
                maths_txt.setEditable(false);
                chemistry_txt.setEditable(false);
                aptitude_txt.setEditable(false);
                english_txt.setEditable(false);
                softSkills_txt.setEditable(false);
                total_marks_txt.setEditable(false);
                
                int m1 = Integer.parseInt(rs.getString(7));
                int m2 = Integer.parseInt(rs.getString(8));
                int m3 = Integer.parseInt(rs.getString(9));
                int m4 = Integer.parseInt(rs.getString(10));
                int m5 = Integer.parseInt(rs.getString(11));
                int m6 = Integer.parseInt(rs.getString(12));
                int m7 = Integer.parseInt(rs.getString(13));
                
                int pass_cutOff = (int)0.3 * 600;
                int count = 0;
                if(m1<30) {
                    count++;
                    physics_txt.setForeground(Color.red);
                }
                if(m2<30) {
                    count++;
                    maths_txt.setForeground(Color.red);
                }
                if(m3<30) {
                    count++;
                    chemistry_txt.setForeground(Color.red);
                }
                if(m4<30) {
                    count++;
                    aptitude_txt.setForeground(Color.red);
                }
                if(m5<30) {
                    count++;
                    english_txt.setForeground(Color.red);
                }
                if(m6<30) {
                    count++;
                    softSkills_txt.setForeground(Color.red);
                }
                
                if(count >= 2 || m7<pass_cutOff) {
                    pass_fail.setText(count + "");
                    pass_fail.setForeground(Color.red);
                }
                else{
                    pass_fail.setText("Pass");
                    pass_fail.setForeground(Color.green);
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

